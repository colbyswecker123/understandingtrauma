function json(data,status=200){
return new Response(JSON.stringify(data),{status,headers:{"Content-Type":"application/json; charset=utf-8","Cache-Control":"no-store"}});
}

function fallbackMessage(){
return fallbackFaithMessages[Math.floor(Math.random()*fallbackFaithMessages.length)];
}

export async function onRequestGet({env}){
try{
if(!env.DB){return json({...fallbackMessage(),source:"fallback"});}
const row=await env.DB.prepare("SELECT message, reference, kind, verse_text, verse_reference FROM faith_messages WHERE status = 'approved' ORDER BY RANDOM() LIMIT 1").first();
if(row&&row.message){
return json({
message:row.message,
reference:row.reference||"",
kind:row.kind||"encouragement",
verse_text:row.verse_text||"",
verse_reference:row.verse_reference||"",
source:"database"
});
}
return json({...fallbackMessage(),source:"fallback"});
}catch(error){
return json({...fallbackMessage(),source:"fallback"});
}
}
